export const translateEmotion = (emotionName: string, targetLang: 'en' | 'es'): string => {
  // Get the emotion ID from the input name (could be in any language)
  const emotionId = emotionMap[emotionName];
  
  // If no mapping found, return original
  if (!emotionId) return emotionName;
  
  // Return translated name
  if (targetLang === 'en') {
    return idToEnglish[emotionId] || emotionName;
  } else {
    return idToSpanish[emotionId] || emotionName;
  }
};
